// ค่าตั้งต้นต่อขั้น
export function levelWpi(levels, cls) {
  const L = levels.find(x => x.cls === Number(cls));
  return L ? L.wpi : 0;
}
// ผลรวมปัจจัยรองที่เลือก (selectedIds = อาเรย์ id)
export function sumFactors(factorList, selectedIds) {
  const set = new Set(selectedIds || []);
  return factorList.filter(f => set.has(f.id)).reduce((s, f) => s + f.add, 0);
}
// ตัวคำนวณหลัก: ค่าตั้งต้นต่อขั้น + ผลรวมปัจจัยรอง
export function repromResult(levels, cls, factorList, selectedIds) {
  const base = levelWpi(levels, cls);
  const add = sumFactors(factorList || [], selectedIds || []);
  return { cls: Number(cls), base, add, wpi: base + add };
}
