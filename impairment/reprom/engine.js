export function iief5Total(answers) {
  return answers.reduce((s, a) => s + clamp(Number(a) || 0, 1, 5), 0);
}
export function iief5Band(total) {
  const t = clamp(Number(total), 1, 25);
  return IIEF5_BANDS.find(b => t >= b.lo && t <= b.hi) || IIEF5_BANDS[IIEF5_BANDS.length - 1];
}
export function iief5Class(total) { return iief5Band(total).cls; }

