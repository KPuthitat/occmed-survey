// คำนวณระดับ (grade) ในชั้น จากปัจจัยหลัก (keyClass) และปัจจัยรอง (secClass)
//   ค่าตั้งต้น = ค่ากลาง (grade B) ของชั้น · ปรับ = secClass − keyClass · clamp ภายในชั้น
export function classGrade(grades, keyClass, secClass) {
  const k = clamp(Math.round(Number(keyClass)), 0, grades.length - 1);
  const row = grades[k];
  if (row.length === 1) {
    return { cls: k, gradeIndex: 0, gradeLetter: null, value: row[0], initial: row[0], net: 0 };
  }
  const mid = Math.floor(row.length / 2); // ค่ากลาง = ระดับ B (index 1)
  const sc = clamp(Math.round(Number(secClass)), 0, grades.length - 1);
  const net = sc - k;
  const gi = clamp(mid + net, 0, row.length - 1);
  return { cls: k, gradeIndex: gi, gradeLetter: GRADE_LETTERS[gi], value: row[gi], initial: row[mid], net };
}

// ตัวคำนวณหลักต่ออวัยวะ (organ = 'vulva'|'uterus'|'ovary')
//   opts.postmenopausal = true → ใช้กติกาพิเศษวัยหมดระดูตาม footnote ของแต่ละตาราง
export function reprofResult(organ, keyClass, secClass, opts = {}) {
  const t = TABLES[organ];
  if (!t) throw new Error('unknown organ: ' + organ);
  const r = classGrade(t.grades, keyClass, secClass);
  const out = { organ, ref: t.ref, ...r, note: null };

  if (opts.postmenopausal) {
    if (organ === 'vulva' && out.cls === 2) {
      // ตาราง 14-1 footnote **: ปัจจัยหลักอยู่ในขั้น 2 แต่วัยหมดระดู → ลดลง 1 ระดับ (เช่น B→A)
      const row = t.grades[out.cls];
      const gi = clamp(out.gradeIndex - 1, 0, row.length - 1);
      out.gradeIndex = gi; out.gradeLetter = GRADE_LETTERS[gi]; out.value = row[gi];
      out.note = 'วัยหมดระดู: ลดลง 1 ระดับ (ตาราง 14-1)';
    } else if (organ === 'uterus' && out.value > 7) {
      // ตาราง 14-2 footnote **: วัยหมดระดู → เพดานสูงสุดที่ร้อยละ 7 (ขั้น 1 ระดับ C)
      out.cls = 1; out.gradeIndex = 2; out.gradeLetter = GRADE_LETTERS[2]; out.value = 7; out.initial = t.grades[1][1];
      out.note = 'วัยหมดระดู: เพดานสูงสุดร้อยละ 7 (ตาราง 14-2)';
    } else if (organ === 'ovary') {
      // ตาราง 14-3 footnote ***: ไม่ประเมินในหญิงวัยหมดระดู/ไม่คาดหวังจะมีบุตรอีก
      out.note = 'วัยหมดระดู/ไม่ประสงค์มีบุตร: ตาราง 14-3 ไม่ประเมินการสูญเสียของรังไข่/ท่อนำไข่';
    }
  }
  return out;
}
