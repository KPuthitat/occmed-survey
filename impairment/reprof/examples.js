import { fx } from '/shared/example.js';

const step = (keyName, keyCls, secCls, initial, net, val, grade, extra) =>
  fx.sheet([
    fx.item('ปัจจัยหลัก', `${keyName} → จัดขั้น`,
      `${fx.eq} ${fx.res('ขั้นที่ ' + keyCls)}${keyCls === 0 || grade == null ? '' : ` ${fx.note('ค่ากลาง ระดับ B = ' + initial + '%')}`}`),
    ...(grade == null ? [] : [
      fx.item('ปัจจัยรอง', `ประวัติ = ขั้น ${secCls} → ปรับ (${secCls}−${keyCls}) = ${net >= 0 ? '+' + net : net}`,
        `${fx.eq} ${net === 0 ? 'ไม่ปรับ' : (net > 0 ? 'เลื่อนขึ้น' : 'เลื่อนลง') + ' ' + Math.abs(net) + ' ระดับ'} ${fx.eq} ${fx.res(val + '%', true)} ${fx.note('ระดับ ' + grade)}`),
    ]),
    ...(extra ? [fx.item('หมายเหตุ', extra, '')] : []),
  ]);

