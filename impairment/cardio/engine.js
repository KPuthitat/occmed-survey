export const GRADE_LETTERS = ['A', 'B', 'C', 'D', 'E'];

// เลือกขั้น (0–5) + ระดับย่อย (gradeIndex 0–4 = A–E) → WPI%
export function cadResult(level, gradeIndex = 0) {
  const L = CAD_LEVELS.find(x => x.level === Number(level));
  if (!L) throw new Error('ไม่พบขั้น ' + level);
  if (L.level === 0) return { level: 0, gradeIndex: null, gradeLetter: null, percent: 0, range: [0, 0], label: '0' };
  const gi = Math.max(0, Math.min(4, Number(gradeIndex)));
  return {
    level: L.level, gradeIndex: gi, gradeLetter: GRADE_LETTERS[gi],
    percent: L.grades[gi], range: L.range,
    label: L.level + GRADE_LETTERS[gi],
  };
}

// ตารางค่ารวม (Combined Values) — เผื่อรวมกับโรค/บทอื่น
export function combineValues(values) {
  const v = values.map(Number).filter(x => x > 0).sort((a, b) => b - a);
  if (!v.length) return 0;
  let acc = v[0];
  for (let i = 1; i < v.length; i++) acc = acc + v[i] * (100 - acc) / 100;
  return Math.min(100, acc);
}
