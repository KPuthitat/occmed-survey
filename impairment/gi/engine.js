export function clamp(x, lo, hi) { return Math.min(hi, Math.max(lo, x)); }

// ปัจจัยรองปรับระดับ A–E ภายในขั้นที่ปัจจัยหลักกำหนด (เริ่มที่ C เสมอ)
export function computeGrade(primaryStage, treatmentStage, weightStage) {
  const p = Number(primaryStage);
  if (p === 0) {
    return {
      stage: 0, gradeIndex: null, gradeLetter: null,
      diffTreat: 0, diffWeight: 0, raw: 0, adj: 0, clamped: false,
      note: 'ขั้น 0 = 0% เสมอ ไม่มีระดับ A–E',
    };
  }
  const diffTreat = Number(treatmentStage) - p;   // ผลต่างเต็มจำนวน
  const diffWeight = Number(weightStage) - p;
  const raw = diffTreat + diffWeight;
  const adj = clamp(raw, -2, 2);                   // ห้ามเกิน 2 ระดับ
  const gradeIndex = 2 + adj;                       // 0=A 1=B 2=C 3=D 4=E
  return {
    stage: p, gradeIndex, gradeLetter: GRADE_LETTERS[gradeIndex],
    diffTreat, diffWeight, raw, adj, clamped: raw !== adj,
  };
}

// คำนวณ % ของตารางหนึ่ง จากขั้นปัจจัยหลัก + ปัจจัยรอง 2 ตัว
export function tableResult(tableId, primaryStage, treatmentStage, weightStage) {
  const t = TABLES[tableId];
  if (!t) throw new Error('ไม่พบตาราง ' + tableId);
  const g = computeGrade(primaryStage, treatmentStage, weightStage);
  const percent = g.stage === 0 ? 0 : t.scale[g.stage][g.gradeIndex];
  const label = g.stage === 0 ? '0' : (g.stage + g.gradeLetter);
  return { tableId, percent, label, ...g };
}

// ตารางค่ารวม (Combined Values): รวม = A + B(100−A)/100 · เรียงมาก→น้อย · ยังไม่ปัดเศษ
export function combineValues(values) {
  const v = values.map(Number).filter((x) => x > 0).sort((a, b) => b - a);
  if (!v.length) return 0;
  let acc = v[0];
  for (let i = 1; i < v.length; i++) acc = acc + v[i] * (100 - acc) / 100;
  return Math.min(100, acc);
}

// รวมทั้งหมด: Combined Values ของค่าอวัยวะก่อน แล้วบวกค่าตาราง 11-6 ตรงๆ · ปัดเศษตอนท้าย · เพดาน 100
export function combineAll(organPercents = [], fistulaPercents = []) {
  const organsCombined = combineValues(organPercents);            // fractional
  const fistulaSum = fistulaPercents.reduce((a, b) => a + Number(b), 0);
  const rawTotal = Math.min(100, organsCombined + fistulaSum);
  return { organsCombined, fistulaSum, rawTotal, total: Math.round(rawTotal) };
}

