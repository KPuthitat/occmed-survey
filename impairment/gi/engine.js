export function standardWeightKg(sex, heightCm, age) {
  const t = STD_WEIGHT[sex];
  if (!t) throw new Error('เพศไม่ถูกต้อง: ' + sex);
  const h = Number(heightCm);
  const col = Number(age) < 25 ? 'y' : 'o';
  const rows = t.rows;
  let row, below = false;
  if (h < rows[0].min) { row = rows[0]; below = true; }
  else { row = rows.find((r) => h >= r.min && h <= r.max) || rows[rows.length - 1]; }
  return {
    kg: row[col], row, below,
    note: below ? 'ส่วนสูงต่ำกว่าช่วงตาราง — ใช้แถวแรก (นอกช่วงตาราง)' : '',
  };
}
