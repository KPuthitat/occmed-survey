// map %ที่ลดลง → ขั้น 0–4 (0=0 · 1=1–4 · 2=5–9 · 3=10–19 · 4=≥20)
export function weightStageFromPercent(pct) {
  const x = Number(pct);
  if (!(x >= 1)) return 0;
  if (x < 5) return 1;
  if (x < 10) return 2;
  if (x < 20) return 3;
  return 4;
}

// %ลด = (มาตรฐาน − ปัจจุบัน) / มาตรฐาน × 100
export function weightLossPercent(standardKg, currentKg) {
  const s = Number(standardKg), c = Number(currentKg);
  if (!(s > 0)) return null;
  return (s - c) / s * 100;
}

