// เลือกขั้น + ระดับย่อย → ร้อยละ
export function urinaryResult(levels, cls, levelIdx) {
  const k = clamp(Math.round(Number(cls)), 0, levels.length - 1);
  const row = levels[k];
  const idx = row.length === 1 ? 0 : clamp(Math.round(Number(levelIdx)), 0, row.length - 1);
  return { cls: k, levelIdx: idx, levelName: row.length === 1 ? null : LEVEL_NAMES[idx], wpi: row[idx] };
}

// รวมค่าการสูญเสีย (Combined Values) — A + B(100−A)/100 · เรียงมาก→น้อย · เพดาน 100
export function combineValues(vals) {
  const xs = (vals || []).map(Number).filter(v => !isNaN(v) && v > 0).sort((a, b) => b - a);
  let acc = 0;
  for (const v of xs) acc = acc + v * (100 - acc) / 100;
  return Math.min(100, Math.round(acc));
}
