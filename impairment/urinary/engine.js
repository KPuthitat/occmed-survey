const clamp = (x, lo, hi) => Math.min(hi, Math.max(lo, x));

export const LEVEL_NAMES = ['ต่ำ', 'กลาง', 'สูง'];

// ============================================================
// eGFR — ช่วงการทำงานของไต (ตาราง 12-1 ข้อ 2 ง.) + การปรับตามอายุ
//   ผู้อายุ > 40 ปี: eGFR ลดลง 6.6 ต่อ 1.73 ตร.ม. ทุกช่วง 10 ปี → บวกคืนเข้ากับค่าที่วัดได้ก่อนจัดขั้น
//   (เช่น อายุ 60 → +13.2 · อายุ 52 → +6.6)
// ============================================================
export const EGFR_BANDS = [
  { cls: 0, lo: 60, hi: Infinity, label: '≥ 60' },
  { cls: 1, lo: 45, hi: 59, label: '45–59' },
  { cls: 2, lo: 30, hi: 44, label: '30–44' },
  { cls: 3, lo: 16, hi: 29, label: '16–29' },
  { cls: 4, lo: 0, hi: 15, label: '≤ 15' },
];
export function egfrAgeAdjust(age) {
  const a = Number(age);
  if (!(a > 40)) return 0;
  return 6.6 * Math.floor((a - 40) / 10);
}
export function effectiveEgfr(egfr, age) {
  return Number(egfr) + egfrAgeAdjust(age);
}
export function egfrClass(effEgfr) {
  const v = Number(effEgfr);
  const b = EGFR_BANDS.find(x => v >= x.lo && v <= x.hi);
  return b ? b.cls : 4;
}

