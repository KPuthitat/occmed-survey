// ตาราง 17-2 — คะแนนรวม PDQ → ระดับความรุนแรง → ร้อยละการสูญเสียทั้งร่างกาย
export const PDQ_BANDS = [
  { lo: 0, hi: 0, label: 'ไม่มี', wpi: 0 },
  { lo: 1, hi: 70, label: 'น้อย', wpi: 0 },
  { lo: 71, hi: 100, label: 'ปานกลาง', wpi: 1 },
  { lo: 101, hi: 130, label: 'มาก', wpi: 2 },
  { lo: 131, hi: 150, label: 'มากที่สุด', wpi: 3 },
];

// รวมคะแนน 15 ข้อ (ข้อที่ไม่ได้ทำ = 0) → 0–150
export function pdqTotal(answers) {
  return (answers || []).slice(0, 15).reduce((s, a) => s + clamp(Number(a) || 0, 0, 10), 0);
}
export function pdqBand(total) {
  const t = clamp(Math.round(Number(total)), 0, 150);
  return PDQ_BANDS.find(b => t >= b.lo && t <= b.hi) || PDQ_BANDS[PDQ_BANDS.length - 1];
}
// ผลรวม: คืนคะแนน + ระดับ + ร้อยละ
export function painResult(totalOrAnswers) {
  const total = Array.isArray(totalOrAnswers) ? pdqTotal(totalOrAnswers) : clamp(Math.round(Number(totalOrAnswers)), 0, 150);
  const band = pdqBand(total);
  return { total, label: band.label, wpi: band.wpi, range: [band.lo, band.hi] };
}
