// helper: สร้างระดับแบบ rangeMode · items = [[level, lo, hi, crit], ...]
function lv(items) {
  return items.map(([level, lo, hi, crit]) => ({ level, range: [lo, hi], crit }));
}
function tbl(id, ref, titleTh, items, note) {
  return {
    id, ref, titleTh, rangeMode: true, keyRow: 'crit',
    factors: [{ key: 'crit', label: 'เกณฑ์การประเมิน — เลือกระดับที่ตรงกับผู้ป่วย' }],
    levels: lv(items), note,
  };
}

